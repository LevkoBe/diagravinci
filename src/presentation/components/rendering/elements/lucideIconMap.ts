export type LucideIconNode = IconNode;

export function getLucideIcon(name: string): IconNode | null {
  return ICONS[name.toLowerCase()] ?? null;
}

const LUCIDE_VIEWBOX = 24;
const ICON_SIZE_RATIO = 0.65;

export function renderLucideIconOnGroup(
  group: Konva.Group,
  nodes: LucideIconNode,
  elementSize: number,
  color: string,
  opacity: number,
): void {
  const targetSize = elementSize * ICON_SIZE_RATIO;
  const scale = targetSize / LUCIDE_VIEWBOX;
  const offset = -targetSize / 2;

  const iconGroup = new Konva.Group({
    x: offset,
    y: offset,
    scaleX: scale,
    scaleY: scale,
    listening: false,
    opacity,
  });

  const commonStroke = {
    stroke: color,
    strokeWidth: 2,
    lineCap: "round" as const,
    lineJoin: "round" as const,
  };

  for (const [tag, attrs] of nodes) {
    switch (tag) {
      case "path":
        iconGroup.add(
          new Konva.Path({
            data: attrs.d,
            fillEnabled: false,
            ...commonStroke,
          }),
        );
        break;
      case "circle":
        iconGroup.add(
          new Konva.Circle({
            x: Number(attrs.cx),
            y: Number(attrs.cy),
            radius: Number(attrs.r),
            fillEnabled: false,
            ...commonStroke,
          }),
        );
        break;
      case "ellipse":
        iconGroup.add(
          new Konva.Ellipse({
            x: Number(attrs.cx),
            y: Number(attrs.cy),
            radiusX: Number(attrs.rx),
            radiusY: Number(attrs.ry),
            fillEnabled: false,
            ...commonStroke,
          }),
        );
        break;
      case "rect":
        iconGroup.add(
          new Konva.Rect({
            x: Number(attrs.x ?? 0),
            y: Number(attrs.y ?? 0),
            width: Number(attrs.width),
            height: Number(attrs.height),
            cornerRadius: Number(attrs.rx ?? attrs.ry ?? 0),
            fillEnabled: false,
            ...commonStroke,
          }),
        );
        break;
      case "line":
        iconGroup.add(
          new Konva.Line({
            points: [
              Number(attrs.x1),
              Number(attrs.y1),
              Number(attrs.x2),
              Number(attrs.y2),
            ],
            ...commonStroke,
          }),
        );
        break;
      case "polyline":
      case "polygon": {
        const pts = attrs.points
          .trim()
          .split(/[\s,]+/)
          .map(Number);
        iconGroup.add(
          new Konva.Line({
            points: pts,
            closed: tag === "polygon",
            fillEnabled: false,
            ...commonStroke,
          }),
        );
        break;
      }
    }
  }

  group.add(iconGroup);
}
